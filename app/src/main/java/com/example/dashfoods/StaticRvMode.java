package com.example.dashfoods;

public class StaticRvMode {


    private final int image;
    private final String text;

    public StaticRvMode(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }

}
